import random
import Apresentacao

def jogar():

    Apresentacao.a_forca()
    tema = escolhe_tema()
    palavra_secreta = carrega_palavra_secreta(tema)

    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)
    
    enforcou = False
    acertou = False
    erros = 0

    while(not enforcou and not acertou):
        
        chute = pede_chute()

        if(chute in palavra_secreta):
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if(acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

    print("\nFim de jogo!")

def escolhe_tema():
    opcao = False
    while(not opcao == 1 or not opcao == 2):
        opcao = input("Escolha o tema!\n(1)Frutas\n(2)Animais\n")
        if(opcao == "1"):
            print("Voce escolheu o tema Frutas\n")
            tema = "Frutas.txt"
            return tema
            break
        elif(opcao == "2"):
            print("Voce escolheu o tema Animais\n")
            tema = "Animais.txt"
            return tema
            break
        else:
            print("\nOpção invalida!!!\n")

def carrega_palavra_secreta(tema):
    
    with open(tema, "r") as arquivo:
        palavras = []

        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta

def inicializa_letras_acertadas(palavra):
    return ["_" for letra in palavra]

def pede_chute():
    chute = input("\nEscolha uma letra: ")
    chute = chute.strip().upper()
    return chute

def marca_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if(chute.upper() == letra.upper()):
            letras_acertadas[index] = letra
        index += 1

